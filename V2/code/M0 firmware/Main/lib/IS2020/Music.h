#define Music_Control_PLAY 0x05  //PLAY command    
#define Music_Control_PAUSE 0x06  //PAUSE command   
#define Music_Control_TOGLE_PLAY_PAUSE 0x07  //PLAY PAUSE toggle   
#define Music_Control_STOP 0x08  //STOP command 

